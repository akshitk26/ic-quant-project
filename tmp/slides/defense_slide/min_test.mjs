import { Presentation, PresentationFile } from '@oai/artifact-tool';
const pres = Presentation.create({ slideSize: { width: 1280, height: 720 } });
const slide = pres.slides.add();
slide.background.fill = '#F6F3EE';
const shape = slide.shapes.add({ geometry: 'rect', x: 100, y: 100, width: 300, height: 100, fill: '#ffffff' });
shape.text.set('hello world');
shape.text.fontSize = 24;
shape.text.color = '#111111';
const pptx = await PresentationFile.exportPptx(pres);
console.log('pptx type', typeof pptx, pptx && Object.keys(pptx), pptx?.constructor?.name);
console.log('pptx props', pptx);
const png = await pres.export({ slide, format: 'png', scale: 1 });
console.log('png type', typeof png, png && Object.keys(png), png?.constructor?.name);
console.log('png props', png);
